public class Design {
  public static void printArtSideBySide() {
    String[] art1 = getArt1();
    String[] art2 = getArt2();

    for (int i = 0; i < Math.max(art1.length, art2.length); i++) {
      String line1 = i < art1.length ? art1[i] : "";
      String line2 = i < art2.length ? art2[i] : "";
      System.out.println(line1 + "    " + line2);
    }
  }

  public static String[] getArt1() {
    return new String[] {
        "       .--.                   .---.",
        "   .---|__|           .-.     |~~~|",
        ".--|===|--|_          |_|     |~~~|--.",
        "|  |===|  |'\\     .---!~|  .--|   |--|",
        "|%%|   |  |.'\\    |===| |--|%%|   |  |",
        "|%%|   |  |\\.'\\   |   | |__|  |   |  |",
        "|  |   |  | \\  \\  |===| |==|  |   |  |",
        "|  |   |__|  \\.'\\ |   |_|__|  |~~~|__|",
        "|  |===|--|   \\.'\\|===|~|--|%%|~~~|--|",
        "^--^---'--^    `-'`---^-^--^--^---'--'"
    };
  }

  public static String[] getArt2() {
    return new String[] {
        "-----------------------------------------------------",
        "······································",
        ": _     _ _                          :",
        ":| |   (_| |__  _ __ __ _ _ __ _   _ :",
        ":| |   | | '_ \\| '__/ _` | '__| | | |:",
        ":| |___| | |_) | | | (_| | |  | |_| |:",
        ":|_____|_|_.__/|_|  \\__,_|_|   \\__, |:",
        ":                              |___/ :",
        "······································"
    };
  }
}
